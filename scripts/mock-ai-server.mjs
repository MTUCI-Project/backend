import { createServer } from 'node:http';

const port = Number(process.env.MOCK_AI_PORT ?? 3001);
const appBackendBaseUrl = process.env.APP_BACKEND_BASE_URL ?? 'http://localhost:3000';

function json(response, status, body) {
    response.writeHead(status, { 'Content-Type': 'application/json; charset=utf-8' });
    response.end(JSON.stringify(body));
}

async function readJson(request) {
    const chunks = [];
    for await (const chunk of request) chunks.push(chunk);
    return JSON.parse(Buffer.concat(chunks).toString('utf8') || '{}');
}

async function recordServiceHistory(userId, message) {
    const response = await fetch(
        `${appBackendBaseUrl}/ai-service/users/${encodeURIComponent(userId)}/chat`,
        {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ message, sentiment: 0 }),
        },
    );
    if (!response.ok) {
        throw new Error(`Application backend history callback returned ${response.status}`);
    }
}

async function loadContext(userId) {
    const response = await fetch(
        `${appBackendBaseUrl}/ai-service/users/${encodeURIComponent(userId)}/context`,
    );
    if (!response.ok) {
        throw new Error(`Application backend context request returned ${response.status}`);
    }
    return response.json();
}

const server = createServer(async (request, response) => {
    try {
        const url = new URL(request.url ?? '/', `http://${request.headers.host}`);
        if (request.method !== 'POST') {
            json(response, 404, { error: 'Route not found' });
            return;
        }

        if (url.pathname === '/ml/chat') {
            const body = await readJson(request);
            if (typeof body.user_id !== 'string' || typeof body.message !== 'string') {
                json(response, 400, { error: 'user_id and message are required' });
                return;
            }

            const context = await loadContext(body.user_id);
            const partnerName = context.facts?.partner?.name ?? 'unknown partner';
            const allergies = context.facts?.partner?.notes?.allergies ?? 'unknown allergies';
            const conversationLength = context.facts?.recent_conversation?.length ?? 0;
            // ChatAgent loads facts before persisting the analyzed user message.
            await recordServiceHistory(body.user_id, body.message);
            json(response, 200, {
                response:
                    `MOCK context: partner=${partnerName}; ` +
                    `allergies=${allergies}; conversation_messages=${conversationLength}; ` +
                    `input="${body.message}"`,
            });
            return;
        }

        if (url.pathname === '/ml/suggest-date') {
            json(response, 200, {
                id: 'mock-date',
                title: 'Mock date',
                description: 'Generated by local mock AI service',
                estimated_cost: Number(url.searchParams.get('budget') ?? 0),
                type: 'outdoor',
            });
            return;
        }

        if (url.pathname === '/ml/recommend') {
            json(response, 200, {
                id: 'mock-gift',
                title: 'Mock gift',
                description: 'Generated by local mock AI service',
                price: 1000,
                partner_url: null,
                reason: 'Mock profile match',
            });
            return;
        }

        if (url.pathname === '/ml/analyze-relationship') {
            json(response, 200, {
                strengths: ['Mock strength'],
                growth_areas: ['Mock growth area'],
                suggested_actions: [{ action: 'Mock action' }],
            });
            return;
        }

        json(response, 404, { error: 'Route not found' });
    } catch (error) {
        json(response, 500, {
            error: error instanceof Error ? error.message : 'Unexpected mock failure',
        });
    }
});

server.listen(port, () => {
    console.log(`Mock AI listening on http://localhost:${port}`);
    console.log(`Application callback target: ${appBackendBaseUrl}`);
});
